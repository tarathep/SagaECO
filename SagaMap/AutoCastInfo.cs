namespace SagaMap
{
    /// <summary>
    /// Defines the <see cref="AutoCastInfo" />.
    /// </summary>
    public class AutoCastInfo
    {
        /// <summary>
        /// Defines the x.
        /// </summary>
        public byte x = byte.MaxValue;

        /// <summary>
        /// Defines the y.
        /// </summary>
        public byte y = byte.MaxValue;

        /// <summary>
        /// Defines the skillID.
        /// </summary>
        public uint skillID;

        /// <summary>
        /// Defines the level.
        /// </summary>
        public byte level;

        /// <summary>
        /// Defines the delay.
        /// </summary>
        public int delay;
    }
}
